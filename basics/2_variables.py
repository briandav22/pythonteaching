#Think of variables as 'buckets' where you can store data in a python program. For example the code that we have listed below stores a some text (a string) into a variable. 

my_string = 'Hello Jose, this is a string stored in a variable'

#we can now just print out the my_string variable and the text 

print(my_string)

# say we wanted to edit this string and add more text to it. We could do this by using the '+' operator. 

my_string += ' I have now added more test to this variable. '

print(my_string)

